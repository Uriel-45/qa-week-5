/**
 * Suma dos números y devuelve el resultado.
 *
 * @param a - El primer número a sumar.
 * @param b - El segundo número a sumar.
 * @returns La suma de a y b.
 *
 * @example
 * ```typescript
 * const resultado = sum(2, 3); // 5
 * ```
 */
export function sum(a: number, b: number): number {
  return a + b;
}
